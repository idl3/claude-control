#!/usr/bin/env python3
"""Hermetic E2E acceptance gate for REAL touch drag-select in the agent-
terminal overlay's copy/select mode.

Companion to `agent-term-drag-select-e2e.py` (#342's BUG 2 gate, which only
proves a synthetic Playwright POINTER drag selects text — Playwright's
`page.mouse.*` dispatches real `mousemove` events, but iOS WebKit does NOT
synthesize `mousemove` during a native finger-drag, so that test is a FALSE
POSITIVE for touch: it never exercised the finger-drag path at all).

This script drives REAL `TouchEvent`s (constructed and dispatched in-page via
`page.evaluate`, bypassing Playwright's input pipeline entirely so no
compat-mouse event can leak in) at `.xterm-screen` and asserts:

  1. In COPY MODE, a touch hold-drag selects the exact partial substring
     (not select-all, not empty) via XtermHost.tsx's new touch->mouse
     synthesis bridge, and the floating Copy button copies it to the
     clipboard.
  2. NEGATIVE CONTROL (confound-killer): the SAME touch-drag sequence in
     TYPING mode selects NOTHING (the bridge effect is `copyMode`-gated, so
     it attaches no listeners in typing mode) and the container still pans
     natively (`scrollLeft` moves) — proving the fix is copy-mode-scoped and
     didn't leak into typing mode, and that the assertion above is actually
     produced by the new touch handler rather than some ambient side effect.

Reuses the hermetic "throwaway server.js instance" idiom + tmux fixture +
coordinate-math helpers from agent-term-drag-select-e2e.py; real :4317 is
only ever polled for health, never touched/restarted.

Run (from repo root, after `npm run build` in web/ so web/dist is fresh):
  python3 web/scratch/agent-term-touch-select-e2e.py
"""

